public class DataTypeShowcase {
    public static void main(String[] args) {
        byte smal = 10;
        int num = 100;
        short num2 = 200;
        long num3 = 100000000L;
        float dig = 11.22f;
        double dig2 = 5.61;
        char car = 'A';
        boolean istrue = true;

        System.out.println("Byte: " + smal);
        System.out.println("integer: " + num);
        System.out.println("short: " + num2);
        System.out.println("long: " + num3);
        System.out.println("float: " + dig);
        System.out.println("double: " + dig2);
        System.out.println("char: " + car);
        System.out.println("boolean: " + istrue);
    }
}
